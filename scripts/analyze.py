def print_results(data: dict) -> None:
    print(f"\n{'='*60}")
    print("[ 질적 기여도 카드 ]")
    print(f"{'='*60}")

    for card in data.get("contribution_cards", []):
        print(f"\n> {card['speaker']}  |  총점: {card['total_score']}점")
        print("-" * 40)
        for c in card["contributions"]:
            print(f"  [{c['type']} +{c['points']}점]")
            print(f"  발언: \"{c['quote']}\"")
            print(f"  근거: {c['reason']}")

    print(f"\n{'='*60}")
    print("[ 약속 카드 (실행 약속) ]")
    print(f"{'='*60}")

    for p in data.get("promise_cards", []):
        deadline = p.get("deadline") or "기한 미정"
        print(f"\n  [약속] {p['speaker']}")
        print(f"     할 일: {p['task']}")
        print(f"     기한 : {deadline}")
        print(f"     발언 : \"{p['quote']}\"")

    print(f"\n{'='*60}")
    print("[ 회의 요약 ]")
    print(f"{'='*60}")
    print(data.get("meeting_summary", ""))


